    private void jbtnReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnReceiptActionPerformed
      //Get random reference number.
        int refs = 123 + (int) (Math.random()*4564);
      // Get date and time
      Calendar timer = Calendar.getInstance();
      timer.getTime();
      SimpleDateFormat tTime = new SimpleDateFormat("HH:mm:ss");
      tTime.format(timer.getTime());
      SimpleDateFormat Tdate = new SimpleDateFormat("dd-MM-yyyy");
      Tdate.format(timer.getTime());
      
      //create receipt
      jtxtReceipt.append("\t McMaw's Restaurant:\n" +
              "Reference:\t\t\t" + refs + 
      "\n--------------------------------------------\n" +
              "Meals:\t\t" + txtCost.getText() + "\n\n" + 
              "Drinks:\t\t" + txtDrink.getText() + "\n\n"+
              "Cost of Items:\t\t" + jtxtTotal.getText() + "\n\n" +
       "--------------------------------------------------" + "\n\n" +
                "Tax:\t\t" + jtxtTax.getText() + "\n\n" +
                "SubTotal:\t\t" + jtxtSubTotal.getText() + "\n\n"+
                "Total:\t\t" + txtTotalTax.getText() +"\n" +
        "----------------------------------------------------"+
                 
                "\nDate: " +Tdate.format(timer.getTime())+
                "\tTime: " +tTime.format(timer.getTime())+
                 "\n\nThank you please call again!");
             
    }//GEN-LAST:event_jbtnReceiptActionPerformed

