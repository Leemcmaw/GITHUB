    //Creating actions for total button
    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
      SubClass ItemCost = new SubClass();
      //Declaring variable types
      double iTotal,iTax,iSubTotal;
      
      ItemCost.BaconBurger = ItemCost.pBaconBurger * Double.parseDouble(jtxtBc.getText());
      ItemCost.ChickenBurger = ItemCost.pChickenBurger * Double.parseDouble(jtxtCb.getText());
      ItemCost.ChickenStack = ItemCost.pChickenStack * Double.parseDouble(jtxtCs.getText());
      ItemCost.Steak = ItemCost.pSteak * Double.parseDouble(jtxtSteak.getText());
      ItemCost.Fish = ItemCost.pFish * Double.parseDouble(jtxtFish.getText());
      
      ItemCost.Coke = ItemCost.pCoke * Double.parseDouble(jtxtCoke.getText());
      ItemCost.Fanta = ItemCost.pFanta * Double.parseDouble(jtxtFanta.getText());
      ItemCost.SevenUp = ItemCost.pSevenUp * Double.parseDouble(jtxtSeven.getText());
      ItemCost.Carlsberg = ItemCost.pCarlsberg * Double.parseDouble(jtxtCarls.getText());
      ItemCost.Sprite = ItemCost.pSprite * Double.parseDouble(jtxtSprite.getText());
      
      iSubTotal = ItemCost.getAmount();
      iTax = ItemCost.cFindTax(iSubTotal);
      iTotal = iSubTotal + iTax;
      // formatting the text to be in £ and 2 dp.
      String SubTotal = String.format("£%.2f", iSubTotal);
      jtxtSubTotal.setText(SubTotal);
      
      String Tax = String.format("£%.2f", iTax);
      jtxtTax.setText(Tax);
             
            
     String Total=String.format("£%.2f", iTotal);
     txtTotalTax.setText(Total);
     
     String Meal=String.format("£%.2f", ItemCost.Meals);
     txtCost.setText(Meal);
     
     String Drink=String.format("£%.2f", ItemCost.Drinks);
     txtDrink.setText(Drink);
     
     String TotalCost=String.format("£%.2f", ItemCost.TotalofMD);
     jtxtTotal.setText(TotalCost);
     //end of total
    }//GEN-LAST:event_jbtnTotalActionPerformed
